/**
 * Abilities reference generator for the Black Market Bazaar mod.
 *
 * Reads BMB_Abilities.xml from Mods/src/Data/GameCore/ and generates
 * a Markdown reference document at docs/references/abilities.md.
 *
 * Parses AbilityBonus → AbilityBonusOption entries. TXT_BMB_* localization
 * keys are resolved to English text. Effects are formatted using the shared
 * formatEffects() helper.
 *
 * Accepts an optional pre-loaded keyMap for use by the umbrella generator
 * (generate-all-references.mjs) to avoid redundant merge/load cycles.
 */

import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { success, error, warn, info, step } from './lib/output.mjs';
import {
  PROJECT_ROOT,
  GAMECORE_DIR,
  ensureFreshBuild,
  loadLocalizationKeys,
  resolveKey,
  escapeCell,
  formatEffects,
} from './lib/reference-helpers.mjs';
import { parseGameCoreXml } from './lib/xml-parser.mjs';

const __filename = fileURLToPath(import.meta.url);
const SOURCE_FILE = 'BMB_Abilities.xml';
const OUTPUT_FILE = path.join(PROJECT_ROOT, 'docs', 'references', 'abilities.md');

// ─── Parse abilities XML ──────────────────────────────────────

/**
 * Parses BMB_Abilities.xml and returns a flat array of ability option objects.
 * Each AbilityBonus may contain multiple AbilityBonusOption children; we
 * flatten them into a single list.
 *
 * @param {Map<string, string>} keyMap
 * @returns {Promise<Array<object>>}
 */
async function parseAbilitiesFile(keyMap) {
  const filePath = path.join(GAMECORE_DIR, SOURCE_FILE);
  if (!existsSync(filePath)) {
    warn(`Source file missing: ${SOURCE_FILE}. Run 'npm run build' to generate it.`);
    return [];
  }

  const xml = await readFile(filePath, 'utf-8');
  const doc = parseGameCoreXml(xml);
  const rawBonuses = doc?.AbilityBonuses?.AbilityBonus;
  if (!rawBonuses) return [];

  const bonuses = Array.isArray(rawBonuses) ? rawBonuses : [rawBonuses];
  const abilities = [];

  for (const bonus of bonuses) {
    const rawOptions = bonus.AbilityBonusOption;
    if (!rawOptions) continue;
    const options = Array.isArray(rawOptions) ? rawOptions : [rawOptions];

    for (const opt of options) {
      const internalName = opt['@_InternalName'] ?? '';
      const rawDisplayName = opt.DisplayName ?? internalName;
      const rawDescription = opt.Description ?? '';
      const modifiers = opt.GameModifier ?? [];

      // Type may be a single string or an array
      const rawType = opt.Type;
      const types = rawType
        ? Array.isArray(rawType) ? rawType : [rawType]
        : [];

      abilities.push({
        internalName,
        displayName: resolveKey(String(rawDisplayName), keyMap),
        description: resolveKey(String(rawDescription), keyMap),
        icon: opt.Icon ?? '—',
        type: types.join(', ') || '—',
        effects: formatEffects(Array.isArray(modifiers) ? modifiers : [modifiers], keyMap),
      });
    }
  }

  return abilities;
}

// ─── Markdown generation ──────────────────────────────────────

function buildAbilityTable(abilities) {
  const lines = [];
  lines.push('| Name | Type | Effects | Description |');
  lines.push('|---|---|---|---|');
  for (const ability of abilities) {
    const name = escapeCell(ability.displayName);
    const type = escapeCell(ability.type);
    const effects = escapeCell(ability.effects);
    const desc = escapeCell(ability.description || '—');
    lines.push(`| ${name} | ${type} | ${effects} | ${desc} |`);
  }
  return lines.join('\n');
}

function generateMarkdown(abilities) {
  const lines = [];

  lines.push('# Black Market Bazaar — Abilities Reference');
  lines.push('');
  lines.push('> Auto-generated by `npm run reference:abilities`. Do not edit manually.');
  lines.push('');
  lines.push(`**${abilities.length} abilities** defined by this mod.`);
  lines.push('');
  lines.push('---');
  lines.push('');
  lines.push(buildAbilityTable(abilities));
  lines.push('');

  return lines.join('\n');
}

// ─── Main entry point ─────────────────────────────────────────

/**
 * Generates the abilities reference document.
 *
 * @param {Map<string, string> | null} keyMap - Pre-loaded localization key map.
 *   If null (standalone mode), runs merge + loads keys itself.
 *   If provided (umbrella mode), skips the merge/load step.
 * @returns {Promise<{ outputFile: string, totalAbilities: number }>}
 */
export async function generateAbilitiesReference(keyMap = null) {
  if (keyMap === null) {
    await ensureFreshBuild();

    step('Loading English localization keys...');
    keyMap = await loadLocalizationKeys();
    if (keyMap.size > 0) {
      step(`  Loaded ${keyMap.size} localization key(s).`);
    } else {
      step('  No localization keys found — TXT_BMB_* keys will appear as-is.');
    }
  }

  info('Parsing abilities XML...');
  step(`  ${SOURCE_FILE}`);
  const abilities = await parseAbilitiesFile(keyMap);
  info(`Parsed ${abilities.length} ability option(s).`);

  // Sort alphabetically
  abilities.sort((a, b) => a.displayName.localeCompare(b.displayName));

  step('Generating Markdown...');
  const markdown = generateMarkdown(abilities);

  const outputDir = path.dirname(OUTPUT_FILE);
  await mkdir(outputDir, { recursive: true });
  await writeFile(OUTPUT_FILE, markdown, 'utf-8');

  console.log('');
  success(`Reference written to: ${path.relative(PROJECT_ROOT, OUTPUT_FILE)}`);

  return { outputFile: OUTPUT_FILE, totalAbilities: abilities.length };
}

// ─── Direct invocation guard ──────────────────────────────────
const isMain =
  process.argv[1] &&
  path.resolve(process.argv[1]) === path.resolve(__filename);

if (isMain) {
  generateAbilitiesReference().catch((err) => {
    error('Unexpected error: ' + err.message);
    process.exit(1);
  });
}
