/**
 * Effects reference generator for the Black Market Bazaar mod.
 *
 * Reads BMB_Effects.xml from Mods/src/Data/GameCore/ and generates
 * a Markdown reference document at docs/references/effects.md.
 *
 * Parses EffectSequencer entries. Effects have no TXT_BMB_* localization —
 * their DisplayName values are plain text. Simple flat table sorted
 * alphabetically by InternalName.
 *
 * Accepts an optional pre-loaded keyMap for API consistency with other
 * generators (it is not used for effects, which have no localization).
 */

import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { success, error, warn, info, step } from './lib/output.mjs';
import {
  PROJECT_ROOT,
  GAMECORE_DIR,
  ensureFreshBuild,
  escapeCell,
} from './lib/reference-helpers.mjs';
import { parseGameCoreXml } from './lib/xml-parser.mjs';

const __filename = fileURLToPath(import.meta.url);
const SOURCE_FILE = 'BMB_Effects.xml';
const OUTPUT_FILE = path.join(PROJECT_ROOT, 'docs', 'references', 'effects.md');

// ─── Parse effects XML ────────────────────────────────────────

/**
 * Parses BMB_Effects.xml and returns a flat array of effect objects.
 *
 * @returns {Promise<Array<object>>}
 */
async function parseEffectsFile() {
  const filePath = path.join(GAMECORE_DIR, SOURCE_FILE);
  if (!existsSync(filePath)) {
    warn(`Source file missing: ${SOURCE_FILE}. Run 'npm run build' to generate it.`);
    return [];
  }

  const xml = await readFile(filePath, 'utf-8');
  const doc = parseGameCoreXml(xml);
  const rawEffects = doc?.EffectBlueprints?.EffectSequencer;
  if (!rawEffects) return [];

  const effects = Array.isArray(rawEffects) ? rawEffects : [rawEffects];

  return effects.map((effect) => {
    const internalName = effect['@_InternalName'] ?? '';

    // Count EmitterBlueprint children
    const rawEmitters = effect.EmitterBlueprint ?? [];
    const emitters = Array.isArray(rawEmitters) ? rawEmitters : [rawEmitters];

    return {
      internalName,
      emitterCount: emitters.length,
      repeatsIndefinitely:
        effect.RepeatIndefinitely === 1 || effect.RepeatIndefinitely === '1',
    };
  });
}

// ─── Markdown generation ──────────────────────────────────────

function buildEffectTable(effects) {
  const lines = [];
  lines.push('| InternalName | Emitters | Repeats Indefinitely |');
  lines.push('|---|---|---|');
  for (const effect of effects) {
    const name = escapeCell(effect.internalName);
    const emitters = String(effect.emitterCount);
    const repeats = effect.repeatsIndefinitely ? 'Yes' : 'No';
    lines.push(`| ${name} | ${emitters} | ${repeats} |`);
  }
  return lines.join('\n');
}

function generateMarkdown(effects) {
  const lines = [];

  lines.push('# Black Market Bazaar — Effects Reference');
  lines.push('');
  lines.push('> Auto-generated by `npm run reference:effects`. Do not edit manually.');
  lines.push('');
  lines.push(`**${effects.length} visual effect(s)** defined by this mod.`);
  lines.push('');
  lines.push('---');
  lines.push('');
  lines.push(buildEffectTable(effects));
  lines.push('');

  return lines.join('\n');
}

// ─── Main entry point ─────────────────────────────────────────

/**
 * Generates the effects reference document.
 *
 * @param {Map<string, string> | null} keyMap - Accepted for API consistency
 *   with other generators but not used (effects have no localization).
 *   If null (standalone mode), runs the merge step to ensure fresh XML.
 * @returns {Promise<{ outputFile: string, totalEffects: number }>}
 */
export async function generateEffectsReference(keyMap = null) {
  if (keyMap === null) {
    await ensureFreshBuild();
  }

  info('Parsing effects XML...');
  step(`  ${SOURCE_FILE}`);
  const effects = await parseEffectsFile();
  info(`Parsed ${effects.length} effect(s).`);

  // Sort alphabetically by InternalName
  effects.sort((a, b) => a.internalName.localeCompare(b.internalName));

  step('Generating Markdown...');
  const markdown = generateMarkdown(effects);

  const outputDir = path.dirname(OUTPUT_FILE);
  await mkdir(outputDir, { recursive: true });
  await writeFile(OUTPUT_FILE, markdown, 'utf-8');

  console.log('');
  success(`Reference written to: ${path.relative(PROJECT_ROOT, OUTPUT_FILE)}`);

  return { outputFile: OUTPUT_FILE, totalEffects: effects.length };
}

// ─── Direct invocation guard ──────────────────────────────────
const isMain =
  process.argv[1] &&
  path.resolve(process.argv[1]) === path.resolve(__filename);

if (isMain) {
  generateEffectsReference().catch((err) => {
    error('Unexpected error: ' + err.message);
    process.exit(1);
  });
}
