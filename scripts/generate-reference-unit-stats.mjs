/**
 * Unit stats reference generator for the Black Market Bazaar mod.
 *
 * Reads BMB_UnitStats.xml from Mods/src/Data/GameCore/ and generates
 * a Markdown reference document at docs/references/unit-stats.md.
 *
 * Parses UnitStatType entries. TXT_BMB_* localization keys are resolved
 * to English text. Simple flat table — no categorisation needed.
 *
 * Accepts an optional pre-loaded keyMap for use by the umbrella generator
 * (generate-all-references.mjs) to avoid redundant merge/load cycles.
 */

import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { success, error, warn, info, step } from './lib/output.mjs';
import {
  PROJECT_ROOT,
  GAMECORE_DIR,
  ensureFreshBuild,
  loadLocalizationKeys,
  resolveKey,
  escapeCell,
} from './lib/reference-helpers.mjs';
import { parseGameCoreXml } from './lib/xml-parser.mjs';

const __filename = fileURLToPath(import.meta.url);
const SOURCE_FILE = 'BMB_UnitStats.xml';
const OUTPUT_FILE = path.join(PROJECT_ROOT, 'docs', 'references', 'unit-stats.md');

// ─── Parse unit stats XML ─────────────────────────────────────

/**
 * Parses BMB_UnitStats.xml and returns a flat array of unit stat objects.
 *
 * @param {Map<string, string>} keyMap
 * @returns {Promise<Array<object>>}
 */
async function parseUnitStatsFile(keyMap) {
  const filePath = path.join(GAMECORE_DIR, SOURCE_FILE);
  if (!existsSync(filePath)) {
    warn(`Source file missing: ${SOURCE_FILE}. Run 'npm run build' to generate it.`);
    return [];
  }

  const xml = await readFile(filePath, 'utf-8');
  const doc = parseGameCoreXml(xml);
  const rawStats = doc?.PlayerAbilityTypes?.UnitStatType;
  if (!rawStats) return [];

  const stats = Array.isArray(rawStats) ? rawStats : [rawStats];

  return stats.map((stat) => {
    const internalName = stat['@_InternalName'] ?? '';
    const rawDisplayName = stat.DisplayName ?? internalName;
    const rawDescription = stat.Description ?? '';

    return {
      internalName,
      displayName: resolveKey(String(rawDisplayName), keyMap),
      description: resolveKey(String(rawDescription), keyMap),
      hidden: stat.Hidden === 1 || stat.Hidden === '1',
      defaultValue: stat.DefaultValue != null ? String(stat.DefaultValue) : '—',
    };
  });
}

// ─── Markdown generation ──────────────────────────────────────

function buildUnitStatTable(stats) {
  const lines = [];
  lines.push('| InternalName | Name | Hidden | Default Value | Description |');
  lines.push('|---|---|---|---|---|');
  for (const stat of stats) {
    const internalName = escapeCell(stat.internalName);
    const name = escapeCell(stat.displayName);
    const hidden = stat.hidden ? 'Yes' : 'No';
    const defaultValue = escapeCell(stat.defaultValue);
    const desc = escapeCell(stat.description || '—');
    lines.push(`| ${internalName} | ${name} | ${hidden} | ${defaultValue} | ${desc} |`);
  }
  return lines.join('\n');
}

function generateMarkdown(stats) {
  const lines = [];

  lines.push('# Black Market Bazaar — Unit Stats Reference');
  lines.push('');
  lines.push('> Auto-generated by `npm run reference:unit-stats`. Do not edit manually.');
  lines.push('');
  lines.push(`**${stats.length} custom unit stat type(s)** defined by this mod.`);
  lines.push('');
  lines.push('---');
  lines.push('');
  lines.push(buildUnitStatTable(stats));
  lines.push('');

  return lines.join('\n');
}

// ─── Main entry point ─────────────────────────────────────────

/**
 * Generates the unit stats reference document.
 *
 * @param {Map<string, string> | null} keyMap - Pre-loaded localization key map.
 *   If null (standalone mode), runs merge + loads keys itself.
 *   If provided (umbrella mode), skips the merge/load step.
 * @returns {Promise<{ outputFile: string, totalStats: number }>}
 */
export async function generateUnitStatsReference(keyMap = null) {
  if (keyMap === null) {
    await ensureFreshBuild();

    step('Loading English localization keys...');
    keyMap = await loadLocalizationKeys();
    if (keyMap.size > 0) {
      step(`  Loaded ${keyMap.size} localization key(s).`);
    } else {
      step('  No localization keys found — TXT_BMB_* keys will appear as-is.');
    }
  }

  info('Parsing unit stats XML...');
  step(`  ${SOURCE_FILE}`);
  const stats = await parseUnitStatsFile(keyMap);
  info(`Parsed ${stats.length} unit stat type(s).`);

  // Sort alphabetically by InternalName
  stats.sort((a, b) => a.internalName.localeCompare(b.internalName));

  step('Generating Markdown...');
  const markdown = generateMarkdown(stats);

  const outputDir = path.dirname(OUTPUT_FILE);
  await mkdir(outputDir, { recursive: true });
  await writeFile(OUTPUT_FILE, markdown, 'utf-8');

  console.log('');
  success(`Reference written to: ${path.relative(PROJECT_ROOT, OUTPUT_FILE)}`);

  return { outputFile: OUTPUT_FILE, totalStats: stats.length };
}

// ─── Direct invocation guard ──────────────────────────────────
const isMain =
  process.argv[1] &&
  path.resolve(process.argv[1]) === path.resolve(__filename);

if (isMain) {
  generateUnitStatsReference().catch((err) => {
    error('Unexpected error: ' + err.message);
    process.exit(1);
  });
}
